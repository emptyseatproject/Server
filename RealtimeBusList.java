package com.dongguk.bus;

public class RealtimeBusList {
	private String arrival_time;
	private String routeid;
	private String stationid;
	private String pno1;
	private String seat1;
	private String sta_order;
	private String route;
	private String sta_idx;
	private String new_flag;
	public String getArrival_time() {
		return arrival_time;
	}
	public void setArrival_time(String arrival_time) {
		this.arrival_time = arrival_time;
	}
	public String getRouteid() {
		return routeid;
	}
	public void setRouteid(String routeid) {
		this.routeid = routeid;
	}
	public String getStationid() {
		return stationid;
	}
	public void setStationid(String stationid) {
		this.stationid = stationid;
	}
	public String getPno1() {
		return pno1;
	}
	public void setPno1(String pno1) {
		this.pno1 = pno1;
	}
	public String getSeat1() {
		return seat1;
	}
	public void setSeat1(String seat1) {
		this.seat1 = seat1;
	}
	public String getSta_order() {
		return sta_order;
	}
	public void setSta_order(String sta_order) {
		this.sta_order = sta_order;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	public String getSta_idx() {
		return sta_idx;
	}
	public void setSta_idx(String sta_idx) {
		this.sta_idx = sta_idx;
	}
	public String getNew_flag() {
		return new_flag;
	}
	public void setNew_flag(String new_flag) {
		this.new_flag = new_flag;
	}
	
}
